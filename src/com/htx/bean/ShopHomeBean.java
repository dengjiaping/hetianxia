package com.htx.bean;

import java.util.List;

//产品Bean
public class ShopHomeBean{
	
	private List<String> id;
	private List<String> title;
	private List<String> brief;
	private List<String> address;
	private List<String> isFocus;
	private List<String> logo;	

	public static ShopHomeBean shopHomeBean;
	
	public ShopHomeBean()
	{
		
	}

	public static ShopHomeBean getInstance()
	{
		  if (shopHomeBean == null) 
	        { 
			  shopHomeBean = new ShopHomeBean(); 
	        } 
	        
	        return shopHomeBean; 
	}
	 
	public List<String> getId() {
		return id;
	}

	public List<String> getTitle() {
		return title;
	}

	public void setTitle(List<String> title) {
		this.title = title;
	}

	public List<String> getBrief() {
		return brief;
	}

	public void setBrief(List<String> brief) {
		this.brief = brief;
	}

	public List<String> getAddress() {
		return address;
	}

	public void setAddress(List<String> address) {
		this.address = address;
	}

	public List<String> getIsFocus() {
		return isFocus;
	}

	public void setIsFocus(List<String> isFocus) {
		this.isFocus = isFocus;
	}

	public List<String> getLogo() {
		return logo;
	}

	public void setLogo(List<String> logo) {
		this.logo = logo;
	}

	public static ShopHomeBean getShopHomeBean() {
		return shopHomeBean;
	}

	public static void setShopHomeBean(ShopHomeBean shopHomeBean) {
		ShopHomeBean.shopHomeBean = shopHomeBean;
	}

	public void setId(List<String> id) {
		this.id = id;
	}
	
}