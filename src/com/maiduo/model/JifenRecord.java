package com.maiduo.model;

public class JifenRecord {

	private String shop;
	private String transactionjifen;
	private String money;
	private String type;
	private String time;
	private String remark;
	
	public String getShop() {
		return shop;
	}
	public void setShop(String shop) {
		this.shop = shop;
	}
	public String getTransactionjifen() {
		return transactionjifen;
	}
	public void setTransactionjifen(String transactionjifen) {
		this.transactionjifen = transactionjifen;
	}
	public String getMoney() {
		return money;
	}
	public void setMoney(String money) {
		this.money = money;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
}
